# numeric data type
a = 100
print(type(a))

# float data type
b = 500.36
print(type(b))

# complex data type
c = 10+4j
print(type(c))

# boolean data type
d = True
e = False
print(type(d),type(e))

# set data type
f = {"rajib","sajib","sagor"}
print(type(f))

# dictionary data type
g = {"key":"value"}
print(type(g))

# list data type
h = [200,500,800,900]
print(type(h))

# tuple data type
j = (20,40,50)
print(type(j))

# string data type
i = "hello how are you all guys"
print(type(i))